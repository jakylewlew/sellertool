    private void GetLatitudeFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_GetLatitudeFieldFocusLost
        // TODO add your handling code here:
          field = null;
          field = GetLatitudeField.getText().trim(); //gets string
        do{
        try{
            this.latitude = Double.valueOf(field);
            System.out.printf("%f", longitude);            
            this.latitude_entered = true;
        }catch(NumberFormatException | InputMismatchException | NullPointerException e){
            //JOptionPane.showInputDialog(e);
             field = (JOptionPane.showInputDialog("re-enter Latitude"));
            }
        if((this.latitude)>90 || (this.latitude <-90)){
            field = (JOptionPane.showInputDialog("re-enter Latitude"));
        }
        }while((this.latitude)>90 || (this.latitude <-90));
    }//GEN-LAST:event_GetLatitudeFieldFocusLost

