    private void GetLongitudeFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_GetLongitudeFieldFocusLost
        // TODO add your handling code here:
        field = null;
        field = GetLongitudeField.getText().trim(); //gets string
        do{
         try{this.longitude = Double.valueOf(field);
            longitude_entered = true;
            output.format("*%f*",longitude);
           
        }catch(NumberFormatException | InputMismatchException | NullPointerException e){
            //JOptionPane.showInputDialog(e);
            field = (JOptionPane.showInputDialog("re-enter Longitude"));            
            }
          if((this.longitude>180) || (this.longitude <-180)){
            field = (JOptionPane.showInputDialog("re-enter Longitude"));
            }
       }while(this.longitude<-180 || this.longitude>180);
    }//GEN-LAST:event_GetLongitudeFieldFocusLost

